import math
def triangle(b, h):
    return print(f'Triangle area = {((b*h)/2.0):.2f}')
def rectangle(l,w):
    return print(f'Rectangle area = {(w*l*1.0):.2f}')
def square(b):
    return print(f'Square area = {math.pow(b,2):.2f}')
def circle(r):
    return print(f'Circle area = {(math.pi)*(math.pow(r,2)):.2f}')


